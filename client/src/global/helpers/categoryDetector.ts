// ===== נרמול =====

// נרמול עברי: מסיר ניקוד, מחליף אותיות סופיות, מסיר סימני פיסוק.
// כך "גבינה!" "גבינה" "גבינה," מתזהות אותו דבר.
function normalize(text: string): string {
  return text
    .toLowerCase()
    .trim()
    // מסיר ניקוד (Unicode Hebrew combining marks)
    .replace(/[֑-ׇ]/g, '')
    // מחליף אותיות סופיות עבריות לרגילות (ך→כ, ם→מ, ן→נ, ף→פ, ץ→צ)
    .replace(/ך/g, 'כ')
    .replace(/ם/g, 'מ')
    .replace(/ן/g, 'נ')
    .replace(/ף/g, 'פ')
    .replace(/ץ/g, 'צ')
    // מסיר סימני פיסוק ומספרים צמודים למילים
    .replace(/[!?.,;:"'`()[\]{}\-_+=/\\|<>]/g, ' ')
    .replace(/\d+%?/g, ' ')  // מספרים (כולל 3%, 5%)
    .replace(/\s+/g, ' ')
    .trim();
}

// שורש עברי פשוט: מסיר סופיות נפוצות כדי "לשבר" רבים/נקבה/זכר.
// "גבינות"→"גבינ", "שקדים"→"שקד", "תפוחים"→"תפוח".
function stem(word: string): string {
  if (word.length < 4) return word;
  const suffixes = ['ים', 'ות', 'יה', 'ית', 'ון', 'נים', 'ני', 'ים', 'ות'];
  for (const suffix of suffixes) {
    if (word.endsWith(suffix) && word.length - suffix.length >= 3) {
      return word.slice(0, -suffix.length);
    }
  }
  return word;
}

// ===== בניית מבני חיפוש =====

interface Lookup {
  // התאמה מדויקת: מילה → קטגוריה
  exact: Map<string, string>;
  // התאמה לפי שורש: שורש → קטגוריה (למילה בודדת)
  stems: Map<string, string>;
  // מילות מפתח ארוכות לחיפוש partial (>=3 תווים)
  phrases: Array<{ keyword: string; category: string }>;
}

let lookup: Lookup | null = null;

function buildLookup(): Lookup {
  if (lookup) return lookup;
  const exact = new Map<string, string>();
  const stems = new Map<string, string>();
  const phrases: Array<{ keyword: string; category: string }> = [];

  for (const [category, keywords] of Object.entries(CATEGORY_KEYWORDS)) {
    for (const raw of keywords) {
      const norm = normalize(raw);
      if (!norm) continue;
      exact.set(norm, category);

      // אם זו מילה בודדת - גם שורש
      if (!norm.includes(' ')) {
        const s = stem(norm);
        if (s && s.length >= 3 && !stems.has(s)) {
          stems.set(s, category);
        }
      }

      // ביטוי בן ≥3 תווים מצטרף לחיפוש partial
      if (norm.length >= 3) {
        phrases.push({ keyword: norm, category });
      }
    }
  }

  // מיון ביטויים - ארוכים קודם (התאמות ספציפיות יותר ראשונות)
  phrases.sort((a, b) => b.keyword.length - a.keyword.length);

  lookup = { exact, stems, phrases };
  return lookup;
}

/**
 * זיהוי קטגוריה לפי שם מוצר.
 * מעביר כמה שלבים מהספציפי לכללי:
 *   1. התאמה מדויקת (אחרי נרמול: "גבינה 5%" = "גבינה")
 *   2. התאמת מילה בודדת מהשם (כל מילה נבדקת)
 *   3. התאמת שורש עברי (רבים/נקבה: "גבינות"→"גבינ")
 *   4. התאמת ביטוי חלקי (מילה מהמילון שמופיעה בתוך השם)
 *   5. ברירת מחדל: "אחר"
 */
export function detectCategory(productName: string): string {
  const { exact, stems, phrases } = buildLookup();
  const norm = normalize(productName);
  if (!norm) return 'אחר';

  // 1. התאמה מדויקת על השם כולו
  const fullMatch = exact.get(norm);
  if (fullMatch) return fullMatch;

  // 2. בדיקת כל מילה בנפרד
  const words = norm.split(/\s+/).filter(w => w.length >= 2);
  for (const word of words) {
    const match = exact.get(word);
    if (match) return match;
  }

  // 3. בדיקת שורש של כל מילה
  for (const word of words) {
    const wordStem = stem(word);
    if (wordStem.length >= 3) {
      const match = stems.get(wordStem);
      if (match) return match;
    }
  }

  // 4. חיפוש חלקי - ביטוי ארוך קודם (יותר ספציפי)
  for (const { keyword, category } of phrases) {
    if (norm.includes(keyword)) return category;
  }

  // 5. חיפוש בין שורשים גם חלקית - "אגוזי" מתוך "אגוזי פקאן"
  for (const word of words) {
    for (const [stemKey, category] of stems) {
      if (word.startsWith(stemKey) || stemKey.startsWith(word)) {
        return category;
      }
    }
  }

  return 'אחר';
}
