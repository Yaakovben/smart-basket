// בניית מפת חיפוש הפוכה (מילה → קטגוריה) בטעינה ראשונה
let lookupMap: Map<string, string> | null = null;

function buildLookupMap(): Map<string, string> {
  if (lookupMap) return lookupMap;
  lookupMap = new Map();
  for (const [category, keywords] of Object.entries(CATEGORY_KEYWORDS)) {
    for (const keyword of keywords) {
      lookupMap.set(keyword.toLowerCase(), category);
    }
  }
  return lookupMap;
}

/**
 * זיהוי קטגוריה לפי שם מוצר
 * סדר: מילון מובנה → חיפוש חלקי → אחר
 */
export function detectCategory(productName: string): string {
  const map = buildLookupMap();
  const name = productName.toLowerCase().trim();

  // התאמה מדויקת
  const exact = map.get(name);
  if (exact) return exact;

  // חיפוש מילה שהמוצר מתחיל בה
  for (const [keyword, category] of map) {
    if (name.startsWith(keyword) || keyword.startsWith(name)) {
      return category;
    }
  }

  // חיפוש מילה שמופיעה בתוך השם
  const words = name.split(/\s+/);
  for (const word of words) {
    const found = map.get(word);
    if (found) return found;
  }

  // חיפוש חלקי
  for (const [keyword, category] of map) {
    if (keyword.length >= 3 && name.includes(keyword)) {
      return category;
    }
  }

  return 'אחר';
}
